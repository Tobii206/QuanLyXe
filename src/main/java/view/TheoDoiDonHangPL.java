    private void btlHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btlHoaDonMouseClicked
        int index = btlHoaDon.getSelectedRow();
        if (index < 0) return;

        Object value = btlHoaDon.getValueAt(index, 0);
        if (value == null) return;

        try {
            String maHoaDon = value.toString();
            loadChiTiet(maHoaDon);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi tải chi tiết: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btlHoaDonMouseClicked

