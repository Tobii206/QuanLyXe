    private void loadHoaDon(int idKH) {
        try {
            List<HoaDon> list = hoaDonRepo.getAll(); // Simplified - get all
            modelHoaDon.setRowCount(0);
            hoaDonIds.clear();
            for (HoaDon hd : list) {
                modelHoaDon.addRow(new Object[]{
                        hd.getMaHoaDon(),
                        hd.getNgayTao(),
                        hd.getTrangThaiDonHang(),
                        String.format("%.0f VND", hd.getTongTien())
                });
                hoaDonIds.add(hd.getMaHoaDon());
            }
            if (list.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không có đơn hàng nào!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi tải đơn hàng: " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void loadChiTiet(String maHoaDon) {
        try {
            List<HoaDonChiTiet> list = chiTietRepo.findById(maHoaDon);
            modelChiTiet.setRowCount(0);
            
            if (list.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Hóa đơn chưa có chi tiết sản phẩm!");
                return;
            }
            
            for (HoaDonChiTiet ct : list) {
                modelChiTiet.addRow(new Object[]{
                        ct.getMaSanPham() != null ? ct.getMaSanPham() : "N/A",
                        ct.getSoLuong(),
                        String.format("%,.0f VND", ct.getDonGia())
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi tải chi tiết: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
